<div class="font-bold text-xl mb-3">1. Objet du traitement</div>

<p>La société Vinotrip, dont le siège est situé à IUT d’Annecy au 9 rue de l’arc en ciel 74960 Annecy, dispose d’un site internet de vente de séjour œnotourisme. Ce site permet à nos clients de réserver et de payer des services d'organisation de séjours.
Les données collectées à cette occasion sont enregistrées et traitées dans un fichier clients.

Ce fichier permet de gérer les réservations ainsi que le paiement pour les séjours œnotouristiques.</p>

<div class="font-bold text-lg mb-3">a) Bases légales des traitements</div>
<ul class="list-disc m-5 justify-center">
    <li>Gestion des réservations : la base légale du traitement est l’exécution d’un contrat (Cf. article 6.1.b) du Règlement européen sur la protection des données).</li>
    <li>Tenue de la comptabilité impliquant la conservation des données pour une période de 10 ans : la base légale du traitement est l’obligation légale (Cf. article 6.1.c du Règlement européen sur la protection des données).</li>
</ul>


<div class="font-bold text-xl mb-3">2. Données collectées</div>

<p>Lorsque vous utilisez nos Services, certaines données à caractère personnel peuvent vous être demandées.

Par « données personnelles », il faut entendre toute information permettant de vous identifier directement ou indirectement, par référence à un numéro d’identification ou à un ou plusieurs éléments qui vous sont propres.

Nous ne demandons pas ces informations si vous ne faites que visiter nos Sites, mais vous ne pourrez accéder à certaines zones de ces sites, ni effectué des réservations, sans vous enregistrer.

Vous serez informé, au moment de la collecte, du caractère facultatif ou obligatoire de la communication des données personnelles que Nous vous demandons et des conséquences éventuelles de la non-communication de ces données.</p>



<div class="font-bold text-lg mb-3">a) Donnée liée à la création d’un compte ?</div>

<p>Lorsque vous créez un compte sur Vinotrip, nous vous demandons de fournir certaines informations personnelles. Ces informations nous aident à protéger votre compte et nous permettent de rendre nos services plus utiles.</p>

<div class="font-bold">Quelles informations nous demandons et pourquoi ?</div>



<div class="font-bold">Nom et Prénom</div>

<p>Veuillez indiquer votre nom de famille ainsi que votre prénom. Ces informations pourront être transmises à certains de nos partenaires (hôtels, restaurants) si nécessaire, et ce, dans le seul but d’assurer le bon déroulement de votre futur séjour Vinotrip.

Lorsque vous publié un avis votre Prénom apparait par default, il vous sera possible d’anonymisé l’avis.</p>

<div class="font-bold">Mot de passe</div>

<p>Pour protéger votre compte, choisissez un mot de passe sécurisé d'au moins dix caractères, composé d’au moins un caractère spécial et un chiffre.</p>

<ul class="list-disc m-5 justify-center">
    <li>Évitez d'utiliser des informations personnelles ou des mots courants qui sont faciles à deviner, tels que le nom de votre rue ou celui de votre animal de compagnie.</li>

    <li>N'utilisez pas un mot de passe que vous avez déjà utilisé pour d'autres comptes ou sites Web. De même, n'utilisez pas ce mot de passe ailleurs.</li>
</ul>

<p>En savoir plus sur les caractéristiques d'un mot de passe sécurisé

Votre mot de passe est traité avec la plus grande confidentialité. Il est crypté et inaccessible à tout salarié de Vinotrip, quel que soit son rôle hiérarchique au sein de l’entreprise.

En cas d'oubli de votre mot de passe, vous pourrez en créer un nouveau via un lien envoyé par email, après vérification de votre identité.</p>



<div class="font-bold">Date de naissance</div>

<p>Saisissez votre date de naissance. Vinotrip propose essentiellement des séjours pour visiter les vignobles. Il est donc obligatoire d’être majeur pour utiliser nos services.

Les autres utilisateurs ne verront pas votre âge, sauf si vous partagez votre date de naissance publiquement ou avec certaines personnes.</p>

<div class="font-bold">Genre</div>

<p>Sélectionnez votre genre, à moins que vous préfériez ne pas le mentionner.

Les autres utilisateurs ne verront pas votre genre, sauf si vous partagez cette information publiquement ou avec certaines personnes.<p>

<div class="font-bold">Numéro de téléphone</div>

<p>Saisissez un numéro de téléphone mobile. Cette information est obligatoire.

En effet après une réservation d’un séjour et pour son organisation nous seront amené à vous contacter par téléphone, pour vous donner des informations spontanées souvent une fois sur place

Si vous oubliez votre mot de passe, votre numéro de téléphone de récupération vous aidera à récupérer l'accès à votre compte.</p>

<div class="font-bold">Adresse e-mail</div>

<p>Si vous disposez déjà d'une adresse e-mail, il est nécessaire de l'ajouter.

Celle-ci servira à sécuriser votre compte, mais aussi a recevoir les facturations.
De plus l’équipe de Vinotrip sera amené à vous contacter par email pour confirmation, et information sur votre réservation/séjour</p>


<div class="font-bold text-lg mb-3">b) Donnée collectée lors d’un achat sur Vinotrip.fr</div>

<div class="font-bold">Informations de paiement</div>

<p>Les informations demandées varient en fonction du moyen de paiement sélectionné. Si vous choisissez un mode de paiement autre que la carte bancaire, vous aurez la possibilité, via un bouton à cocher, de sauvegarder vos données bancaires.

Dans le respect du RGPD, seul le cryptogramme ne sera pas enregistré dans notre base de données. Vous devrez donc le saisir à nouveau pour chaque futur achat.

Vos données bancaires sont encryptées et bénéficient d'une protection maximale. Elles seront conservées jusqu'à l'expiration de la carte bancaire ou jusqu'à ce que vous décidiez de modifier ou supprimer l'enregistrement de vos informations.</p>

<div class="font-bold">Adresse postale</div>

<p>Une adresse postale vous sera demandée. Vous aurez la possibilité de renseigner deux adresses : une pour votre domicile et une autre pour la facturation. Ces adresses peuvent être identiques

Si vous le souhaitez, vous pourrez sauvegarder vos adresses en validant un bouton d'option.

Conformément à la réglementation RGPD, les adresses sauvegardées pourront être consultées, modifiées ou supprimées à tout moment sur votre page de profil.
Les adresses enregistrées seront uniquement utilisées pour faciliter vos futurs achats sur Vinotrip.</p>


<div class="font-bold">Historique des achats</div>

<p>Nous conservons l'historique de vos achats effectués sur nos sites, comprenant la date, le montant, les articles achetés et le moyen de paiement utilisé. Vous avez la possibilité de consulter l'intégralité de votre historique d'achats depuis la page de votre profil.</p>


<div class="font-bold text-lg mb-3">c) Obligation de comptabilité :</div>

<p>Il est important de souligner que certaines données personnelles doivent être conservées pendant une durée de 10 ans, conformément aux obligations légales relatives à la conservation des factures. Ces informations comprennent :</p>

<ul class="list-disc m-5 justify-center">
    <li>Nom Prénom</li>
    <li>Adresse</li>
    <li>Mode de paiement</li>
    <li>Séjours commandés</li>
    <li>Date de paiement</li>
</ul>

<p>Celle-ci seront archivé sous forme de facture dissocié du fichier client.</p>

<p>De toute évidence ses données ne seront plus traitées dans notre base de données et uniquement conservé pour des obligations légales.</p>



<div class="font-bold text-xl mb-3">3. Destinataires des données</div>
<ul class="list-disc m-5 justify-center">
    <li>Les services clients et facturation de la société Vinotrip sont destinataires de l’ensemble des catégories de données.</li>
    <li>Ses sous-traitants, chargés de l’organisation des différents séjours (hôtel, restaurant et autres partenaire), sont destinataires de l’identité, de l’adresse et du numéro de téléphone de nos clients.</li>
</ul>

<div class="font-bold text-xl mb-3">4. Durée de conservation des données</div>
<ul class="list-disc m-5 justify-center">
    <li>Données nécessaires à la gestion des commandes et à la facturation : pendant toute la durée de la relation commerciale et dix (10) ans au titre des obligations comptables.</li>
    <li>Données nécessaires à la réalisation des actions de fidélisation et à la prospection : pendant toute la durée de la relation commerciale et trois (3) ans à compter du dernier achat.</li>
    <li>Données relatives aux moyens de paiement : conservées pendant une durée de trois (3) ans à compter de la date du dernier achat ou jusqu'à l'expiration de la date de validité du moyen de paiement, selon la première éventualité.</li>
</ul>
